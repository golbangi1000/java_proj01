package chapter03.ex06;

public class ConditionalOperator {

	public static void main(String[] args) {
		// 3항 연산자 
		
		
		/*
		 *  (조건)? 참: 거짓; (조건) 참이면 ? 뒤의 <<참>>을 실행, 거짓이명 <<거짓>> 실행 
		 */
		
		int value1 = (3>5) ? 6: 9;
		System.out.println(value1);
		
		int value2 = ( 3< 5) ? 6 : 9;
		System.out.println(value2);
		
		

		
		
	}

}
